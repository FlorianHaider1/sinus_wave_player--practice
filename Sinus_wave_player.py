# Music Player using sound card to play simple melodys.
# Practice oop
# Features:
# - Notes insertable by calling it its 'natural' name, so C, D, E, etc.
# - adding flat/sharp, by adding #/b to the Notes, maybe o for normal
# - adding length of notes by adding a Number, i.e. 16, 8, 4, 2, 1
# - adding pauses: P
# - Notes then saved in tuples, (C,o,8)(P,o,1)(E,b,8) for (C Eightnote), (Full Pause), (Eb Eightnote) 
# - Radiobutton for 60, 90, 120, 150 bpm, changing the length of notes accordingly